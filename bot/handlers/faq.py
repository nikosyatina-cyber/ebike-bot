def get_back_button(section: str = "main") -> InlineKeyboardBuilder:
    """Возвращает кнопку «Назад»."""
    builder = InlineKeyboardBuilder()
    if section == "main":
        builder.row(types.InlineKeyboardButton(
            text="🔙 Закрыть",
            callback_data="faq:close",
        ))
    else:
        builder.row(types.InlineKeyboardButton(
            text="🔙 Назад к разделам",
            callback_data="faq:back_to_menu",
        ))
    return builder


@router.message(Command("faq"))
@router.message(Command("help"))
@router.message(Command("помощь"))
async def cmd_faq(message: types.Message) -> None:
    """Открывает главное меню FAQ."""
    await message.reply(
        "❓ <b>FAQ — ЧАСТО ЗАДАВАЕМЫЕ ВОПРОСЫ</b>\n\n"
        "Выберите интересующий раздел:",
        reply_markup=get_faq_keyboard(),
        parse_mode="HTML",
    )


@router.callback_query(F.data == "faq:close")
async def faq_close(callback: types.CallbackQuery) -> None:
    """Закрывает FAQ."""
    await callback.answer()
    await callback.message.delete()


@router.callback_query(F.data == "faq:back_to_menu")
async def faq_back(callback: types.CallbackQuery) -> None:
    """Возвращает к списку разделов FAQ."""
    await callback.answer()
    await callback.message.edit_text(
        "❓ <b>FAQ — ЧАСТО ЗАДАВАЕМЫЕ ВОПРОСЫ</b>\n\n"
        "Выберите интересующий раздел:",
        reply_markup=get_faq_keyboard(),
        parse_mode="HTML",
    )


@router.callback_query(F.data.startswith("faq:"))
async def faq_section(callback: types.CallbackQuery) -> None:
    """Показывает конкретный раздел FAQ."""
    await callback.answer()

    section_key = callback.data.split(":")[1]

    if section_key in ["close", "back_to_menu"]:
        return

    section = FAQ_SECTIONS.get(section_key)

    if section is None:
        await callback.message.answer("❌ Раздел не найден.")
        return

    builder = get_back_button(section_key)

    await callback.message.edit_text(
        f"{section['title']}\n\n{section['text']}",
        reply_markup=builder.as_markup(),
        parse_mode="HTML",
    )